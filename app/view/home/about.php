							<p style="text-indent: 3em;">Cursar uma Graduação exige, entre outras coisas, organização e planejamento. Fazendo uma média dos cursos
								existentes na Pontifícia Universidade Católica do Rio de Janeiro, a duração de um curso de Graduação costuma ser
								estimada em 4/5 anos e, para a conclusão de tal curso, um aluno precisa somar no currículo, em média, 235 créditos
								curriculares.
							</p>
							<p style="text-indent: 3em;">
								Para conseguir se aproximar dessa estimativa, e formar-se dentro de um período de tempo esperado,
								é necessário que se planeje previamente cada período. Se um aluno, por exemplo, pretende se formar em exatamente
								4 anos em um curso que totaliza N créditos ele deve tentar cursar, em média, N/8 créditos por período para que não
								ultrapasse o tempo de conclusão estimado. Para o curso de ciência de computação, por exemplo, o total de créditos é 212,
								que para uma conclusão em 8 períodos resulta em uma média de 26.5 créditos por período. Por outro lado, um aluno que
								tenha tempo para cursar, no máximo, 20 créditos por período em um curso de 220 deve ficar atento ao fato de que
								a conclusão se dará em apenas 11 períodos, ou cinco anos e meio.
							</p>
							<p style="text-indent: 3em;">
								O Plan-on é um sistema planejador de curso que auxilia alunos de Graduação dos cursos de Engenharia e de Computação
								da PUC-Rio a planejar sua grade curricular do primeiro ao último período a ser cursado. Um usuário pode criar
								diferentes simulações de curso. Cada simulação de curso pode ter várias simulações de período.
							</p>
